/* --------------------------------------------------------------------------------------------------------------------------------------------------- */
/* Error Handler */
/* --------------------------------------------------------------------------------------------------------------------------------------------------- */
class ErrorHandler {
    constructor(error) {
        if(error) {
            this.throwException(error);
        };
        return false;
    };
    throwException(error) {
        return console.error(error);
    };
};
/* --------------------------------------------------------------------------------------------------------------------------------------------------- */
/* Exports */
/* --------------------------------------------------------------------------------------------------------------------------------------------------- */
module.exports = ErrorHandler;