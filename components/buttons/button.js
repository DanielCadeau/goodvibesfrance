const Button = ({ Id = undefined, Type = "callToAction", Text = "Button", IconClass = null, OnClick = undefined }) => {
    return <button id={ Id } data-button={ Type } onClick={ OnClick }>
        <span>
            { (IconClass) ? <i className={ IconClass }></i> : Text }
        </span>
    </button>;
};
/* --------------------------------------------------------------------------------------------------------------------------------------------- */
/* Exports */
/* --------------------------------------------------------------------------------------------------------------------------------------------- */
export default Button;